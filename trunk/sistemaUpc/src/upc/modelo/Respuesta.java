package upc.modelo;

public class Respuesta {
private int idrespuesta;
private String texto;
private int idusuario;
private int comentarios_idcomentarios;

public int getIdrespuesta() {
	return idrespuesta;
}
public void setIdrespuesta(int idrespuesta) {
	this.idrespuesta = idrespuesta;
}
public String getTexto() {
	return texto;
}
public void setTexto(String texto) {
	this.texto = texto;
}
public int getIdusuario() {
	return idusuario;
}
public void setIdusuario(int idusuario) {
	this.idusuario = idusuario;
}
public int getComentarios_idcomentarios() {
	return comentarios_idcomentarios;
}
public void setComentarios_idcomentarios(int comentarios_idcomentarios) {
	this.comentarios_idcomentarios = comentarios_idcomentarios;
}

 


}
